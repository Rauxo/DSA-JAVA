public class TypesOfOperators {

    public static void main(String args[]){
        //Arithmetic Binary (+,-,/,*,%) and unary (++,--)
        int a = 10 ;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a%b);

        int c = a++;
        System.out.println(c);

        //Relational (==,!= , > , < , >= , <=)
        System.out.println(a == b);
        System.out.println(a != b);

        //Logical
    }
}