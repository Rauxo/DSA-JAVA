public class DataTypes {

    public static void main(String args[]){
        byte b = 8; 
        System.out.println(b);
        char ch = 'a'; 
        System.out.println(ch);
        boolean bool = true; 
        System.out.println(bool);
        float fl = 10.5f;
        System.out.println(fl);
        int number = 20;
        System.out.println(number);
    }
}